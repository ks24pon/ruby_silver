a = [1, 2, 5, 7, 8]
b = [1, 3, 6, 7, 9]
c = nil || a & b | a && a | b
p c

# aがnilではないので、後ろのaがそのまま残る
# => [1, 2 ,5, 7, 8]
a && a

# aとbの共通の要素だけを配列にする
a & b #=> [1, 7]

# 重複を除いて両方に結合
[1, 7] | [1, 2, 5, 7, 8] #=> [1, 2, 5, 7, 8]

# 再び重複を除いて追加
[1, 7, 2, 5, 8] | [1, 3, 6, 7, 9]
# => [1, 7, 2, 5, 8, 3, 6, 9]

# 最後：nil || 上記の結果
# nil || 値は右和がの値を返す
c = nil || [1, 7, 2, 5, 8, 3, 6, 9]

# 出力
p c
